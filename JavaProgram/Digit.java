import java.util.Scanner;
public class Digit {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter one integer");
		int a=sc.nextInt();
		if
		(a>=-9&&a<=9)
		{
			System.out.println("integer is a one digit number");
		}
		else
		{
			System.out.print("integer is a two digit number");
		}

	}

}
